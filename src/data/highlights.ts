export const getHighlightsByDomain = (domainName: string) =>
  highlights.filter((h) => h.domainName === domainName);

export const getLikedHighlights = () => highlights.filter((h) => h.liked);

export const toggleHighlight = (highlightId: string): Highlight | null => {
  const highlight = highlights.find((h) => h.id === highlightId);
  if (highlight) {
    highlight.liked = !highlight.liked;
  }
  return highlight || null;
};
